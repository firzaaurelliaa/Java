public class Variabel {
    public static void main(String[] args) {
        int nilai = 10;
        double nilai_2 = 5.3;
        int nilai_3 = 100;
        // int hasil = nilai_3 / nilai;
        double hasilPenjumlahan = nilai + nilai_2;
        double hasilPengurangan = nilai - nilai_2;
        String s = "Belajar Java";
        System.out.println("Hasil penjumlahan : " + hasilPenjumlahan);
        System.out.println("Hasil pengurangan : " + hasilPengurangan);
        System.out.println("Kita sedang " + s);
        System.out.println("Hasilnya pembagian : " + nilai_3 / nilai);
    }
}
